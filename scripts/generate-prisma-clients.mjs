import { spawnSync } from 'node:child_process';
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const canonicalSchemaPath = path.join(projectRoot, 'prisma', 'schema.prisma');
const generatedSchemaDir = path.join(projectRoot, 'prisma', '.generated');
const neonSchemaPath = path.join(generatedSchemaDir, 'schema.neon.prisma');
const prismaBin = path.join(
  projectRoot,
  'node_modules',
  '.bin',
  process.platform === 'win32' ? 'prisma.cmd' : 'prisma',
);

function replaceExactlyOnce(source, pattern, replacement, label) {
  const flags = pattern.flags.includes('g') ? pattern.flags : `${pattern.flags}g`;
  const matches = source.match(new RegExp(pattern.source, flags));
  if (matches?.length !== 1) {
    throw new Error(
      `Expected exactly one ${label} in prisma/schema.prisma, found ${matches?.length ?? 0}`,
    );
  }
  return source.replace(pattern, replacement);
}

function generate(schemaPath) {
  const result = spawnSync(prismaBin, ['generate', '--schema', schemaPath], {
    cwd: projectRoot,
    encoding: 'utf8',
    stdio: 'inherit',
  });
  if (result.error) throw result.error;
  if (result.status !== 0) {
    throw new Error(`prisma generate failed for ${path.relative(projectRoot, schemaPath)}`);
  }
}

const canonicalSchema = readFileSync(canonicalSchemaPath, 'utf8');
let neonSchema = replaceExactlyOnce(
  canonicalSchema,
  /generator\s+client\s*\{([\s\S]*?)\}/,
  (_block, body) =>
    `generator client {${body}  output     = "../../src/generated/prisma/neon"\n}`,
  'client generator',
);
neonSchema = replaceExactlyOnce(
  neonSchema,
  /(datasource\s+db\s*\{[\s\S]*?provider\s*=\s*)"sqlite"/,
  '$1"postgresql"',
  'SQLite datasource provider',
);

mkdirSync(generatedSchemaDir, { recursive: true });
writeFileSync(
  neonSchemaPath,
  `// AUTO-GENERATED by scripts/generate-prisma-clients.mjs. Do not edit.\n${neonSchema}`,
);

generate(canonicalSchemaPath);
generate(neonSchemaPath);
