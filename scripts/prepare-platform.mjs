import { mkdirSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const generatedDir = path.join(projectRoot, 'src', 'generated', 'platform');
const requestedTarget = process.env.DEPLOY_TARGET?.trim().toLowerCase();
const deployTarget =
  requestedTarget === 'vercel' || (!requestedTarget && process.env.VERCEL === '1')
    ? 'vercel'
    : 'cloudflare';
const suffix = deployTarget === 'vercel' ? 'vercel' : 'cloudflare';
const banner =
  `// AUTO-GENERATED by scripts/prepare-platform.mjs for ${deployTarget}. Do not edit.\n`;

mkdirSync(generatedDir, { recursive: true });
writeFileSync(
  path.join(generatedDir, 'prisma.ts'),
  `${banner}export { prisma } from '@/lib/prisma.${suffix}';\n`,
);
writeFileSync(
  path.join(generatedDir, 'media-storage.ts'),
  `${banner}export * from '@/lib/media-storage.${suffix}';\n`,
);

console.log(`Prepared platform modules for ${deployTarget}`);
